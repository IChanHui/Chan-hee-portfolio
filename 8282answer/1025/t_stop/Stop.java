package t_stop;

public class Stop {
	private int num_m;
	private int type;
	private String date;
	private int set_num;
	public int getNum_m() {
		return num_m;
	}
	public void setNum_m(int num_m) {
		this.num_m = num_m;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public int getSet_num() {
		return set_num;
	}
	public void setSet_num(int set_num) {
		this.set_num = set_num;
	}
	
	
}
